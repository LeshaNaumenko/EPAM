package project1_mobile_service.view;

/**
 * {@code ConstantsMessage} interface contains messages to inform the user.
 *
 * @author Alex Naumenko
 * @version 1.0
 */

public interface ConstantsMessage {

    /**
     * String that represents message to user about wrong input
     */
    String WRONG_INPUT_INT_DATA = "Wrong input. Repeat please.";

    /**
     * String that represents message to user that there are no such tariffs.
     */
    String NO_TARIFF = "No tariff ! ";

    /**
     * String that represents message to user about resulting table.
     */
    String RESULT = "Resulting table:";
    /**
     * String that represents message to user about total number of clients.
     */
    String NUMBER_OF_CLIENTS = "Total number of clients ";
    /**
     * String that represents message to user to enter a positive number.
     */
    String POSITIVE = "Enter a positive number >>";
    /**
     * String that represents message to user to enter a name of tariff.
     */
    String ENTER_NAME_OF_TARIFF = "Enter name of tariff>>";
}

